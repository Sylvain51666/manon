function loadProjectsForLanguage(language) {
	if (projects) {
		projectsCurrentLanguage = projects.hasOwnProperty(language)
			? projects[language]
			: projects["en"];
	}
}

function storeCurrentFilter(filter) {
	localStorage.setItem("portfolio-filter", filter);
}

function storedCurrentFilter() {
	return localStorage.getItem("portfolio-filter");
}

function loadProject(id) {
	return projectsCurrentLanguage.filter((x) => {
		return x.id === id;
	});
}

function getNextId(current, filter) {
	const prjs = getFilteredProjectArray(filter.substring(1));
	const currentIdx = prjs.indexOf(current);
	var nextIdx = Math.min(prjs.length - 1, currentIdx + 1);
	if (currentIdx === nextIdx) nextIdx = 0;
	return prjs[nextIdx];
}

function getPreviousId(current, filter) {
	const prjs = getFilteredProjectArray(filter.substring(1));
	const currentIdx = prjs.indexOf(current);
	const prevIdx = currentIdx - 1 < 0 ? prjs.length - 1 : currentIdx - 1;
	return prjs[prevIdx];
}

function getFilteredProjectArray(filter) {
	var prjs;
	if (filter != "*" && filter.length > 0) {
		prjs = projectsCurrentLanguage
			.filter((x) => {
				return x.type.indexOf(filter) >= 0;
			})
			.map((y) => {
				return y.id;
			});
	} else
		prjs = projectsCurrentLanguage.map((x) => {
			return x.id;
		});
	return prjs;
}
